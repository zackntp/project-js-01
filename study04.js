// type conversion การแปลงชนิดข้อมูล
// คือ การเปลี่ยนชนิดข้อมูลจากชนิดหนึ่งเป็นอีกชนิดหนึ่ง
//------------------------------
// string conversion
let aa = '100'

console.log(Number(aa) / 5);
console.log(Number(aa) * 5);
console.log(Number(aa) + 5);
console.log(Number(aa) - 5);

// number conversion
let dd = 100
let cc = 200.123123123123

console.log( dd.toString() + cc.toString());
console.log( dd.toFixed(4));
console.log( cc.toFixed(2));


// boolean conversion

let ee = true
let ff = false

console.log(Number(ee));
console.log(Number(ff));

